#include "libft.h"

char    *ft_substr(char const *s, unsigned int start, size_t len)
{
    char    *subs;
    size_t  s_len;

    if (!s)
        return (NULL);
    s_len = ft_strlen(s);
    if (start >= s_len)
        return (ft_strdup(""));
    if (len > s_len - start)
        len = s_len - start;
    subs = (char *)malloc(sizeof(char) * (len + 1));
    if (!subs)
        return (NULL);
    ft_strlcpy(subs, s + start, len + 1);
    return (subs);
}
